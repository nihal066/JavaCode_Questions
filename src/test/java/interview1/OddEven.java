package interview1;

public class OddEven {
    public static void main(String[] args) {
        OddEven(33);
    }
    public static void OddEven(int number){
        if(number%2==0){
            System.out.println("the "+number+" is odd");
        }
        else{
            System.out.println("the "+number+" is even");
        }
    }
}

